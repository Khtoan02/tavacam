<script>
    // Lucide Icons - Initialize after library load
    if (typeof lucide !== 'undefined') {
        lucide.createIcons();
    }

    // Header Scroll Effect
    document.addEventListener('DOMContentLoaded', () => {
        const header = document.getElementById('masthead');
        if (header) {
            const handleScroll = () => {
                if (window.scrollY > 20) {
                    header.classList.add('is-scrolled');
                } else {
                    header.classList.remove('is-scrolled');
                }
            };

            window.addEventListener('scroll', handleScroll);
            handleScroll(); // Check on load
        }
        
        // Mobile Menu Toggle - IMPROVED
        const mobileMenuToggle = document.getElementById('mobile-menu-toggle');
        const mobileMenu = document.getElementById('mobile-menu');
        const mobileMenuClose = mobileMenu?.querySelector('button[onclick*="classList.add"]');
        
        if (mobileMenuToggle && mobileMenu) {
            // Toggle mobile menu
            mobileMenuToggle.addEventListener('click', (e) => {
                e.stopPropagation();
                const isHidden = mobileMenu.classList.contains('hidden');
                
                if (isHidden) {
                    mobileMenu.classList.remove('hidden');
                    mobileMenu.classList.add('flex');
                    document.body.style.overflow = 'hidden'; // Prevent scroll
                } else {
                    mobileMenu.classList.add('hidden');
                    mobileMenu.classList.remove('flex');
                    document.body.style.overflow = ''; // Restore scroll
                }
            });
            
            // Close button inside menu
            if (mobileMenuClose) {
                mobileMenuClose.addEventListener('click', () => {
                    mobileMenu.classList.add('hidden');
                    mobileMenu.classList.remove('flex');
                    document.body.style.overflow = '';
                });
            }
            
            // Close when clicking backdrop (not the sidebar)
            mobileMenu.addEventListener('click', (e) => {
                if (e.target === mobileMenu) {
                    mobileMenu.classList.add('hidden');
                    mobileMenu.classList.remove('flex');
                    document.body.style.overflow = '';
                }
            });
            
            // Close when clicking any menu link
            const menuLinks = mobileMenu.querySelectorAll('a');
            menuLinks.forEach(link => {
                link.addEventListener('click', () => {
                    setTimeout(() => {
                        mobileMenu.classList.add('hidden');
                        mobileMenu.classList.remove('flex');
                        document.body.style.overflow = '';
                    }, 150);
                });
            });
        }
    });

    // Language Switcher Logic
    function setLanguage(lang) {
        const textEl = document.getElementById('current-lang-text');
        const flagEl = document.getElementById('current-lang-flag');

        if (lang === 'vn') {
            textEl.textContent = 'Vietnam (VN)';
            flagEl.innerHTML = '<img src="https://flagcdn.com/w20/vn.png" width="14" alt="VN" class="shrink-0">';
            localStorage.setItem('language', 'vn');
        } else {
            textEl.textContent = 'Global (EN)';
            flagEl.innerHTML = '<img src="https://flagcdn.com/w20/us.png" width="14" alt="US" class="shrink-0">';
            // Alternatively use Globe icon for default if preferred, but flag is consistent 
            // flagEl.innerHTML = '<i data-lucide="globe" class="w-3 h-3"></i>'; 
            localStorage.setItem('language', 'en');
        }
    }

    // Load saved language
    document.addEventListener('DOMContentLoaded', () => {
        const savedLang = localStorage.getItem('language');
        if (savedLang) {
            setLanguage(savedLang);
        }
    });
</script>

